package stage_1;

/*
判断语句 if
 */
public class t_if_18 {
    public static void main(String[] args) {
        System.out.println("今天天气不错，正在压马路，突然发现一个快乐的地方：网吧");
        int age = 17;
        if (age >= 18) {
            System.out.println("可以进入网吧");
        }else{
            System.out.println("年龄不够，你滚");
        }
        System.out.println("肥嘎吃饭");
    }

}
